    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {
            int selectedRow = jTable1.getSelectedRow();
            jTextField1.setText(String.valueOf(jTable1.getValueAt(selectedRow, 1)));
            getSelectedBatchID = String.valueOf(jTable1.getValueAt(selectedRow, 0));
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String batchName = jTextField1.getText();
        if (batchName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "batch Name is Empty", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            MySQL.execute("UPDATE batch SET `batchName` = '" + batchName + "' WHERE batchID = '" + getSelectedBatchID + "' ");
            JOptionPane.showMessageDialog(this, "Successfully Updated", "success", JOptionPane.INFORMATION_MESSAGE);
            loadTable();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String batchName = jTextField2.getText();
        ResultSet checkData = MySQL.execute("SELECT * FROM batch WHERE batchName='" + batchName + "' ");

        try {
            if (checkData.next()) {
                JOptionPane.showMessageDialog(this, "Batch Already Exist", "warning", JOptionPane.WARNING_MESSAGE);
            } else {
                if (batchName.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "batch Name is Empty", "warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.execute("INSERT INTO batchID(batchName) VALUES ('" + batchName + "') ");
                    JOptionPane.showMessageDialog(this, "Successfully Added", "success", JOptionPane.INFORMATION_MESSAGE);
                    loadTable();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(batchMain.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

