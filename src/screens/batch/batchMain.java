    public void loadTable() {
        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        try {
            ResultSet classTable = MySQL.execute("SELECT * FROM `batch`");
            tm.setRowCount(0);
            while (classTable.next()) {
                Vector v = new Vector<>();
                v.add(classTable.getString("batchID"));
                v.add(classTable.getString("batchName"));
                tm.addRow(v);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

