    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {
            int selectedRow = jTable1.getSelectedRow();
            jLabel2.setText(String.valueOf(jTable1.getValueAt(selectedRow, 0)));
            selectedSubjectID = String.valueOf(jTable1.getValueAt(selectedRow, 0));
            jTextField1.setText(String.valueOf(jTable1.getValueAt(selectedRow, 1)));
            jTextField2.setText(String.valueOf(jTable1.getValueAt(selectedRow, 2)));
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String subjectName = jTextField1.getText();
        String subjectPrice = jTextField2.getText();
        if (subjectName.isEmpty() || subjectPrice.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fill All details", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            MySQL.execute("UPDATE subject SET `description` = '" + subjectName + "', price = '" + Double.valueOf(subjectPrice) + "' WHERE subno = '" + selectedSubjectID + "'");
            JOptionPane.showMessageDialog(this, "Success", "success", JOptionPane.INFORMATION_MESSAGE);
            loadTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

