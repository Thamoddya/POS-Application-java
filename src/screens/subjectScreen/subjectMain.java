    public void loadTable() {
        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        try {
            ResultSet classTable = MySQL.execute("SELECT * FROM subject ");
            tm.setRowCount(0);
            while (classTable.next()) {
                Vector v = new Vector<>();
                v.add(classTable.getString("subno"));
                v.add(classTable.getString("description"));
                v.add(classTable.getString("price"));
                tm.addRow(v);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

