    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String getClassID = classIDGet.getText();
        LocalDateTime date = dateTimePicker1.getDateTimePermissive();
        int SubjectID = jComboBox2.getSelectedIndex();
        int TeacherID = jComboBox1.getSelectedIndex();

        try {
            long classID = Long.parseLong(getClassID);

            if (String.valueOf(classID).length() >= 10) {
                jLabel5.setText("Class ID must be less than 10 digits");
            } else {
                if (date == null || getClassID.isEmpty()) {
                    jLabel5.setText("Fill all data");
                } else {
                    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
                    String formattedDate = date.format(formatter);

                    String query1 = "SELECT * FROM class WHERE classNO = '" + classID + "'";
                    String rs = checkSame.check(query1);

                    if (rs.equals("equal")) {
                        jLabel5.setText("Class ID exists");
                    } else {
                        String query2 = "INSERT INTO class(classNO,timeslot,subject_Subno,teacher_TeacherNo) VALUES ('" + classID + "','" + formattedDate + "' ,'"+SubjectID+"' , '"+TeacherID+"')";
                        MYSQL.insert(query2);
                        System.out.println("Success");
                        classIDGet.setText("");
                        jLabel7.setText("Sclass Added Successfullt");
                        jLabel7.setForeground(Color.ORANGE);
                    }
                }
            }
        } catch (NumberFormatException e) {
            jLabel5.setText("Class ID is always a number");
            System.out.println(e);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

