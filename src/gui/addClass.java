    public void loadSubjects() {
        String query = "SELECT * FROM subject";

        try {
            ResultSet rs = MYSQL.search(query);
            Vector<String> subjects = new Vector<>();
            subjects.add(0, "SELECT");

            while (rs.next()) {
                int id = rs.getInt("Subno");
                String description = rs.getString("description");
                subjects.add(id, description);
            }

            DefaultComboBoxModel<String> dcm = new DefaultComboBoxModel<>(subjects);
            jComboBox2.setModel(dcm);
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public void loadTeachers() {
        String query = "SELECT * FROM teacher";

        try {
            ResultSet rs = MYSQL.search(query);
            Vector<String> teachers = new Vector<>();
            teachers.add(0, "SELECT");

            while (rs.next()) {
                int teacherID = rs.getInt("TeacherNo");
                String TeacherName = rs.getString("name");
                teachers.add(teacherID, TeacherName);
            }
            DefaultComboBoxModel<String> dcm = new DefaultComboBoxModel<>(teachers);
            jComboBox1.setModel(dcm);
        } catch (Exception e) {
            System.out.println(e);
        }

    }

