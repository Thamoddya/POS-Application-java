    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String fullName = jTextArea1.getText();
        String address = jTextArea2.getText();
        String teacherNumber = jTextArea3.getText();
        String subject = jComboBox1.getSelectedItem().toString();

        if (fullName.isEmpty() || address.isEmpty() || teacherNumber.isEmpty()) {
            jLabel11.setText("Fill all data");
        } else {
            try {
                long teacherID = Long.parseLong(teacherNumber);

                if (String.valueOf(teacherID).length() >= 10) {
                    jLabel11.setText("Teacher ID must be less than 10 digits");
                } else {
                    String query = "INSERT INTO teacher(TeacherNo, Adress, subject, name) VALUES ('" + teacherID + "', '" + address + "', '" + subject + "', '" + fullName + "')";
                    MYSQL.insert(query);
                    jLabel11.setText("Successfully Registered.");

                    jTextArea1.setText("");
                    jTextArea2.setText("");
                    jTextArea3.setText("");
                }
            } catch (NumberFormatException e) {
                jLabel11.setText("Teacher ID is always a number");
                System.out.println(e);
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

