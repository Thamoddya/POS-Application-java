    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String TeacherNo = jTextField1.getText();
        String query = "SELECT * FROM teacher WHERE TeacherNo = '" + TeacherNo + "'";

        try {
            ResultSet studentData = MYSQL.search(query);

            jTextField4.setText("");
            jTextField3.setText("");

            studentData.next();
            jTextField4.setText(studentData.getString("name"));
            jTextField3.setText(studentData.getString("Adress"));

            String query2 = "SELECT * FROM subject WHERE description <> '" + studentData.getString("subject") + "'";

            try {
                ResultSet rs = MYSQL.search(query2);
                Vector<String> v = new Vector<>();
                v.add(studentData.getString("subject"));

                while (rs.next()) {
                    v.add(rs.getString("description"));
                }

                DefaultComboBoxModel<String> dcm = new DefaultComboBoxModel<>(v);
                jComboBox2.setModel(dcm);
            } catch (Exception e) {
                System.out.println(e);
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

