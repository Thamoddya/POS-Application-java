    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String studentID = jTextField1.getText();
        String query = "SELECT * FROM student WHERE studentNo = '" + studentID + "'";

        try {
            ResultSet studentData = MYSQL.search(query);

            jTextArea1.setText("");
            jTextArea2.setText("");
            jTextArea3.setText("");

            studentData.next();
            jTextField2.setText(studentData.getString("name"));
            jTextField3.setText(studentData.getString("address"));
            jDateChooser2.setDate(studentData.getDate("dob"));
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

