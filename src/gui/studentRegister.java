    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String fullName = jTextArea1.getText();
        String address = jTextArea2.getText();
        String studentIdString = jTextArea3.getText();

        if (fullName.isEmpty() || address.isEmpty() || studentIdString.isEmpty()) {
            jLabel11.setText("Fill all data");
        } else {
            try {
                long studentId = Long.parseLong(studentIdString);

                if (String.valueOf(studentId).length() >= 10) {
                    jLabel11.setText("Student ID must be less than 10 digits");
                } else {
                    java.util.Date getDate = jDateChooser1.getDate();
                    java.sql.Date dateOfBirth = new java.sql.Date(getDate.getTime());

                    String query = "INSERT INTO student(studentNo, address, name, dob) VALUES ('" + studentId + "', '" + address + "', '" + fullName + "', '" + dateOfBirth + "')";
                    MYSQL.insert(query);
                    jLabel11.setText("Successfully Registered.");

                    jTextArea1.setText("");
                    jTextArea2.setText("");
                    jTextArea3.setText("");
                }
            } catch (NumberFormatException e) {
                jLabel11.setText("Student ID is always a number");
                System.out.println(e);
            }
        }


    }//GEN-LAST:event_jButton4ActionPerformed

